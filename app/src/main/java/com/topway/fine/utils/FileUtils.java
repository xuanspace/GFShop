    /**
     * 拷贝Image文件，并且以MD5命名
     * @param fileName
     * @return
     */
    public static String copyImageFile(String fileName,String dirPath) {
        File source = new File(fileName);
        if (!source.isFile()) {
            return null;
        }

        MessageDigest digest = null;
        FileInputStream in = null;
        FileOutputStream out = null;
        byte buffer[] = new byte[1024];
        int len;

        String postfix = fileName.substring(fileName.lastIndexOf(".")+1);
        String uuid = UUID.randomUUID().toString();
        File target = new File(dirPath + uuid + "." + postfix);

        try {
            digest = MessageDigest.getInstance("MD5");
            in = new FileInputStream(source);
            out = new FileOutputStream(target);
            while ((len = in.read(buffer, 0, 1024)) != -1) {
                digest.update(buffer, 0, len);
                out.write(buffer, 0, len);
            }
            in.close();
            out.flush();
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
        BigInteger bigInt = new BigInteger(1, digest.digest());
        String md5 = bigInt.toString(16);

        // change to name md5 value
        File md5File = new File(dirPath + md5 + "." + postfix);
        if (!md5File.exists())
            target.renameTo(new File(dirPath + md5 + "." + postfix));
        return md5File.getName();
    }


    /**
     * RandomAccessFile 获取文件的MD5值
     *
     * @param fileName 文件路径
     * @return md5
     */
    public static String getFileMd5(String fileName) {
        MessageDigest messageDigest;
        RandomAccessFile randomAccessFile = null;

        try {
            messageDigest = MessageDigest.getInstance("MD5");
            File file = new File(fileName);
            if (!file.isFile() || !file.exists())
                return "";

            randomAccessFile=new RandomAccessFile(file,"r");
            byte[] bytes=new byte[1024*1024*10];
            int len=0;
            while ((len=randomAccessFile.read(bytes))!=-1){
                messageDigest.update(bytes,0, len);
            }
            BigInteger bigInt = new BigInteger(1, messageDigest.digest());
            String md5 = bigInt.toString(16);
            while (md5.length() < 32) {
                md5 = "0" + md5;
            }
            return md5;
        } catch (NoSuchAlgorithmException e) {
            e.printStackTrace();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                if (randomAccessFile != null) {
                    randomAccessFile.close();
                    randomAccessFile = null;
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }

        return "";
    }

